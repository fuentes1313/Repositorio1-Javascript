// Define a function isFive that will return true if a number is equal to 5 and false if it is not.

function isFive (number){
    if (number === 5){
        return true;
    }
    else {
        return false;
    }
}

// para imprimir en la consola el valor
console.log (isFive(5));
console.log (isFive(13));
